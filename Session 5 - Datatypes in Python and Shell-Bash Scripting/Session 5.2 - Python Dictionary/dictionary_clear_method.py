"""
Method - clear()

Description: Removes all items from the dictionary.
"""

# Creating a dictionary for Electronics Shop Inventory
inventory = {"Laptop": 10, "Smartphone": 15, "Tablet": 5, "Headphones": 20}

# Printing Electronics Shop Inventory
print('Electronics Shop Inventory:', inventory)

# Clear the Electronics Shop Inventory
inventory.clear()

# Printing Electronics Inventory after clearing it
print('Electronics Shop Inventory after clearing it:', inventory)


"""
                        Output - 
---------------------------------------------------------------
Electronics Shop Inventory: {'Laptop': 10, 'Smartphone': 15, 'Tablet': 5, 'Headphones': 20}
Electronics Shop Inventory after clearing it: {}
"""

