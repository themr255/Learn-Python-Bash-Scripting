"""
Method - index()

Description: Returns the index of the specified element in the tuple.
"""

# Creating cartoon_characters tuple containing cartoon characters
cartoon_characters = ('Turtle', 'Rabbit', 'Unicorn')
print("Cartoon Characters Tuple:", cartoon_characters)

# Getting index of 'Unicorn' in vowels
index_of_unicorn = cartoon_characters.index('Unicorn')

print("Index of Unicorn in Cartoon Characters Tuple:", index_of_unicorn)

"""
                        Output - 
---------------------------------------------------------------
Cartoon Characters Tuple: ('Turtle', 'Rabbit', 'Unicorn')
Index of Unicorn in Cartoon Characters Tuple: 2
"""

