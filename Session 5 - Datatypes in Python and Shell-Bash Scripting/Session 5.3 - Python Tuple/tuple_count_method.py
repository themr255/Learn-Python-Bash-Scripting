"""
Method - count()

Description: Returns the number of times the specified element appears in the tuple.
"""

# Creating cartoon_characters tuple containing cartoon characters
cartoon_characters = ('Turtle', 'Rabbit', 'Unicorn', 'Rabbit')
print("Cartoon Characters Tuple:", cartoon_characters)

# counts the number of times Rabbit appeared in the tuple
count_of_rabbit = cartoon_characters.count('Rabbit')

# Printing how many times 'Rabbit' appeared in cartoon_characters tuple
print("Count of Rabbit in Cartoon Characters Tuple:", count_of_rabbit)

"""
                        Output - 
---------------------------------------------------------------
Cartoon Characters Tuple: ('Turtle', 'Rabbit', 'Unicorn', 'Rabbit')
Count of Rabbit in Cartoon Characters Tuple: 2
"""

