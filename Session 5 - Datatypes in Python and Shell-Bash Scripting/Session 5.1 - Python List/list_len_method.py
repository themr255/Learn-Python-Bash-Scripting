"""
Method - len()

Description: The len() method returns the number of items present in the list.
"""

# Creating a list for Punjabi Menu
PUNJABI = ["Dal Makhni", "Rajma", "Paneer Tikka Masala", "Paneer Makhni", "Butter Roti", "Butter Naan", "Pudhina Paratha"]

# Counting number of items present in PUNJABI list
count_of_items = len(PUNJABI)

# Finally, printing Count of Items in Punjabi Menu List
print("Count of Items in Punjabi Menu: {}".format(count_of_items))

"""
                        Output - 
---------------------------------------------------------------
Count of Items in Punjabi Menu: 7
"""